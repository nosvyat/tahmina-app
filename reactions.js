function hashReactionString(str) {
  let hash = 0;

  for (let i = 0; i < str.length; i += 1) {
    hash = (hash << 5) - hash + str.charCodeAt(i);
    hash |= 0;
  }

  return Math.abs(hash);
}

function getUniversalReaction(questionId, answerValue) {
  const source = `${questionId}:${answerValue}`;
  const index = hashReactionString(source) % UNIVERSAL_REACTIONS.length;
  return UNIVERSAL_REACTIONS[index];
}

function getReactionForAnswer(questionId, answerValue) {
  if (!questionId || !answerValue) {
    return "";
  }

  const questionMap = REACTIONS_BY_QUESTION[questionId];

  if (questionMap && questionMap[answerValue]) {
    return questionMap[answerValue];
  }

  return getUniversalReaction(questionId, answerValue);
}

